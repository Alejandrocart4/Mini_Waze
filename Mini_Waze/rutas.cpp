void Rutas::dibujarRuta(const QString& origen, const QString& destino, QGraphicsScene* scene,QListWidget* Lw_Rutas, QColor colorLinea) {
    if (!scene || !Lw_Rutas) {
        qDebug() << "Error: Scene o ListWidget no están inicializados.";
        return;
    }

    QVector<Ruta> rutasDisponibles = obtenerRutas();

    qDebug() << "Total de rutas disponibles en dibujarRuta:" << rutasDisponibles.size();

    if (rutasDisponibles.isEmpty()) {
        qDebug() << "No hay rutas en obtenerRutas().";
        return;
    }

    bool rutaEncontrada = false;
    for (const Ruta& ruta : rutasDisponibles) {
        if (!ruta.inicio || !ruta.fin) {
            qDebug() << "Ruta inválida encontrada: " << ruta.nombre;
            continue;
        }

        if ((ruta.inicio->nombre == origen && ruta.fin->nombre == destino) ||
            (ruta.inicio->nombre == destino && ruta.fin->nombre == origen)) {

            qDebug() << "Ruta encontrada: " << ruta.nombre;

            QPainterPath path;
            path.moveTo(ruta.puntos.first());
            for (const QPointF& punto : ruta.puntos) {
                path.lineTo(punto);
            }

            scene->addPath(path, QPen(colorLinea, 3, Qt::SolidLine, Qt::RoundCap, Qt::RoundJoin));

            QListWidgetItem* item = new QListWidgetItem(ruta.nombre);
            Lw_Rutas->addItem(item);
        }
    }

    if (!rutaEncontrada) {
        //QMessageBox::critical(this, "Error", "No se encontró una ruta ");
        qDebug() << "No se encontró una ruta entre" << origen << "y" << destino;
    }
}

void Rutas::seleccionarRuta(QListWidget* Lw_Rutas, QLabel* Lb_KmRestante, QLabel* Lb_TiempoRestante, QLabel* Lb_HoraLlegada) {
    if (!Lw_Rutas || !Lb_KmRestante || !Lb_TiempoRestante || !Lb_HoraLlegada) {
        qDebug() << "Error: Algún QLabel o QListWidget es nulo.";
        return;
    }

    int index = Lw_Rutas->currentRow(); // Obtener el índice seleccionado

    QVector<Ruta> rutasDisponibles = obtenerRutas();

    if (index < 0 || index >= rutasDisponibles.size()) {
        qDebug() << "Error: Índice de ruta fuera de rango -> " << index;
        return;
    }

    Ruta rutaSeleccionada = rutasDisponibles[index]; // Obtener la ruta seleccionada

    if (!rutaSeleccionada.inicio || !rutaSeleccionada.fin) {
        qDebug() << "Error: Ruta seleccionada con punteros nulos.";
        return;
    }

    Lb_KmRestante->setText(QString("Distancia: %1 km").arg(rutaSeleccionada.distancia));
    Lb_TiempoRestante->setText(QString("Tiempo: %1 min").arg(rutaSeleccionada.tiempo));
    Lb_HoraLlegada->setText("Hora de llegada estimada: 18:00");

    qDebug() << "Ruta seleccionada correctamente: " << rutaSeleccionada.nombre;
}

QVector<QPointF> Rutas::calcularRutaOptima(const QString& origen, const QString& destino, bool porTiempo) {
        QVector<Ruta> rutas = obtenerRutas();
        QMap<QString, double> distancia;
        QMap<QString, QString> previo;
        QSet<QString> visitados;
        QMap<QString, QVector<QPointF>> rutasOptimas;

        for (const Ruta& ruta : rutas) {
            distancia[ruta.inicio->nombre] = std::numeric_limits<double>::infinity();
            distancia[ruta.fin->nombre] = std::numeric_limits<double>::infinity();
        }

        distancia[origen] = 0;

        while (!visitados.contains(destino)) {
            QString actual;
            double minDist = std::numeric_limits<double>::infinity();
            for (const auto& ciudad : distancia.keys()) {
                if (!visitados.contains(ciudad) && distancia[ciudad] < minDist) {
                    minDist = distancia[ciudad];
                    actual = ciudad;
                }
            }

            if (actual.isEmpty()) break;
            visitados.insert(actual);

            for (const Ruta& ruta : rutas) {
                if (ruta.inicio->nombre == actual && !visitados.contains(ruta.fin->nombre)) {
                    double peso = porTiempo ? ruta.tiempo : ruta.distancia;
                    double nuevaDistancia = distancia[actual] + peso;

                    if (nuevaDistancia < distancia[ruta.fin->nombre]) {
                        distancia[ruta.fin->nombre] = nuevaDistancia;
                        previo[ruta.fin->nombre] = actual;
                        rutasOptimas[ruta.fin->nombre] = ruta.puntos;
                    }
                }
            }
        }

        QVector<QPointF> rutaFinal;
        QString nodo = destino;

        while (previo.contains(nodo)) {
            rutaFinal = rutasOptimas[nodo] + rutaFinal;
            nodo = previo[nodo];
        }
        return rutaFinal;
    }
